            if (saturno.isSelected() == true) {
                guest.questaoMod3(true);
            } else {
                guest.questaoMod3(false);
            }
            ImageIcon icon = new ImageIcon(MapMundiOceanos[3].getMap());
            imagemMOD2.setIcon(icon);
            urano.setText("Oceano índico");
