    public void Modifica() {
        int i1 = 0, i2 = 0, i3 = 0, i4 = 0, i5 = 0;
        int pc = RND.nextInt(14);
        int pc1 = RND.nextInt(4);
        while (i1 == 0) {
            if (pc1 == 0 || pc1 < 3) {
                i1 = 1;
                saturno.setText(oceano.oceano[pc1]);
            }
            else
                pc1 = RND.nextInt(4);
        }
        while (i2 == 0) {
            if (pc == 0 || pc < 5) {
                i2 = 1;
                jupiter.setText(pais.Pais[pc]);
            }
            else
                pc = RND.nextInt(14);
        }
        while (i3 == 0) {
            if (pc > 4 || pc < 10) {
                i3 = 1;
                urano.setText(pais.Pais[pc]);
            }
            else
                pc = RND.nextInt(14);
        }
        while (i4 == 0) {
            if (pc1 > 2 || pc1 < 5) {
                i4 = 1;
                marte.setText(oceano.oceano[pc1]);
            }
            else
                pc1 = RND.nextInt(4);
        }
        while (i5 == 0) {
            if (pc1 > 4 || pc1 < 7) {
                i5 = 1;
                mercurio.setText(continente.Continente[pc1]);
            }
            else
                pc = RND.nextInt(14);
        }
    }

